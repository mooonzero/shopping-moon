    @Test
    @DisplayName("상품 Querydsl 조회 테스트2")
    public void queryDslTest2(){

        this.createItemList2();

        // BooleanBuilder : query에 들어갈 조건을 만들어주는 빌더
        BooleanBuilder booleanBuilder = new BooleanBuilder();
        QItem item = QItem.item;
        String itemDetail = "테스트 상품 상세 설명";
        int price = 10003;
        String itemSellStat = "SELL";

        booleanBuilder.and(item.itemDetail.like("%"+itemDetail+"%"));
        booleanBuilder.and(item.price.gt(price));
        //gt 찾기
        System.out.println("gt========>"+item.price.gt(price));
        // 출력 결과 : gt========>item.price > 10003

        if(StringUtils.equals(itemSellStat, ItemSellStatus.SELL)){
            booleanBuilder.and(item.itemSellStatus.eq(ItemSellStatus.SELL));
        }

        //PageRequest.of(a,b) 메소드를 사용해 pageable 객체 생성
        //a는 조회할 페이지의 번호 , b는 한 페이지 당 조회할 데이터의 개수
        Pageable pageable = PageRequest.of(0,5);
        Page<Item> itemPagingResult =
                itemRepository.findAll(booleanBuilder, pageable);
        System.out.println("total elements : " + itemPagingResult.getTotalElements());

        List<Item> resulItemList = itemPagingResult.getContent();
        for (Item resultItem: resulItemList){
            System.out.println(resultItem.toString());
        }
    }
